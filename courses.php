  <p> The University’s general courses along with their detailed descriptions are discussed here. <br/><br/> 
  
  <Strong>CS 5333 Discrete Structures (3 semester hours)</strong><br/><br/>
Mathematical foundations of computer science.  Logic, sets, relations, graphs and algebraic structures. Combinatorics and metrics for performance evaluation of algorithms. (3-0) <br/><br/>

<Strong>Operating Systems Concepts (3 semester hours)</strong><br/><br/>
Processes and threads. Concurrency issues including semaphores, monitors and deadlocks.  Simple memory management.  Virtual memory management.  CPU scheduling algorithms.  I/O management.  File management.  Introduction to distributed systems. Prerequisites: CS 5330 and CS 5343 (may be taken concurrently) and a working knowledge of C and Unix.  (3-0)<br/><br/>

<Strong>CS 5349 Automata Theory (3 semester hours)</strong><br/><br/>
Deterministic and nondeterministic finite automata; regular expressions, regular sets, context-free grammars, pushdown automata, context free languages.  Selected topics from Turing Machines and undecidability.  Prerequisite: CS 5333. (3-0)<br/><br/>

<Strong>CS 5375 Principles of UNIX (3 semester hours)</strong><br/><br/>
Design and history of the UNIX operating system.  Detailed study of process and file system data structures. Shell programming in UNIX.  Use of process-forking functionality of UNIX to simplify complex problems.  Interprocess communication and coordination. Device drivers and streams as interfaces to hardware features.  TCP/IP and other UNIX inter-machine communication facilities.  Prerequisite: CS 3335. (3-0) <br/><br/>

<Strong>CS 6301 Special Topics in Computer Science(3 semester hours) </strong><br/><br/>
Topics will vary. (May be repeated for credit) (3-0)<br/><br/>

<Strong>CS 6320 Natural Language Processing (3 semester hours) </strong><br/><br/>
This course covers state-of-the-art methods for natural language processing.  After an introduction to the basics of syntax, semantic, and discourse analysis, the focus shifts to the integration of these modules into natural-language processing systems.  In addition to natural language understanding, the course presents advanced material on lexical knowledge acquisition, natural language generation, machine translation, and parallel processing of natural language. Prerequisite: CS 5343. (3-0) <br/><br/>

<Strong>CS 6322 Information Retrieval (3 semester hours)</strong><br/><br/>
This course covers modern techniques for storing and retrieving unformatted textual data and providing answers to natural language queries.  Current research topics and applications of information retrieval in data mining, data warehousing, text mining, digital libraries, hypertext, multimedia data, and query processing are also presented.  Prerequisite: CS 5343. (3-0) <br/><br/>

<Strong>CS 6375 Machine Learning (3 semester hours)</strong><br/><br/>
Algorithms for training perceptions and multi-layer neural nets: back propagation, Boltzmann machines, and self-organizing nets. The ID3 and the Nearest Neighbor algorithms. Formal models for analyzing learnability: exact identification in the limit and probably approximately correct (PAC) identification. Computational limitations of learning machines. Prerequisite: CS 5343. (3-0) <br/><br/>

<Strong>SE 6360 (CS 6360) Database Design (3 semester hours)</strong><br/><br/>
Methods, principles, and concepts that are relevant to the practice of database software design. Database system architecture; conceptual database models; relational and object-oriented databases; database system implementation; query processing and optimization; transaction processing concepts, concurrency, and recovery; security.  Prerequisite: CS 5343. (3-0) <br/><br/>

<Strong>CS 6363 (CE 6363) Design and Analysis of Computer Algorithms (3 semester hours)</strong><br/><br/>
The study of efficient algorithms for various computational problems.  Algorithm design techniques.  Sorting, manipulation of data structures, graphs, matrix multiplication, and pattern matching.  Complexity of algorithms, lower bounds, NP completeness.  Prerequisite: CS 5343. (3-0)<br/><br/>

